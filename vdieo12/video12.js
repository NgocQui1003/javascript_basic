// WHILE, DO WHILE
let arr = ['qui', 'hue', 'banh beo', 'de thuong']

// đối với dòng lập for
for(let i =0; i < arr.length; i++)
{
    console.log('gia tri trong mang voi i = ', i , 'la: ' , arr[i]);
}

// đối với vòng lập while
let i = 0;
while(i < arr.length)
{
    console.log('gia tri mang trong vong lap while voi i = ', i , 'la: ' , arr[i]);
    i++;
}